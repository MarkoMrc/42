#include <unistd.h>

int	ft_str_is_printable(char *str)
{
	int	i;
	int	r;

	r = 0;
	i = 0;
	while (str[i])
	{
		if (!(str[i] >= ' ' && str[i] <= '~'))
		{
			r = 0;
			break ;
		}
		else
		{
			r = 1;
		}
		i++;
	}
	return (r);
}
