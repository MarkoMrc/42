#include <unistd.h>

int	ft_str_is_printable(char *str)
{
	int	i;
	int	r;

	r = 0;
	i = 0;
	if (str[0] == '\0')
	{
		r = 1;
		break ;
	}
	while (str[i])
	{
		if (str[i] > 31 && str[i] < 127)
		{
			r = 1;
		}
		else
		{
			r = 0;
			break ;
		}
		i++;
	}
	return (r);
}
