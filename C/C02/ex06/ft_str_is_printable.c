#include <unistd.h>

int	ftr_is_lowercase(char *str)
{
	int	i;

	i = 0;
	while (str[i] || str[0] == '\0')
	{
		if (str[i] >= '32' && str[i] <= '127')
		{
			return (1);
		}
		if (str[i] == '\0')
		{
			return (1);
		}
		i++;
	}
	return (0);
}
