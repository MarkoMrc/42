#include <unistd.h>

int	ft_str_is_numeric(char *str)
{
	int	i;
	int	r;

	r = 0;
	i = 0;
	if (str[0] == '\0')
		r = 1;
	while (str[i])
	{
		if (str[i] >= '0' && str[i] <= '9')
			r = 1;
		else
		{
			r = 0;
			break ;
		}
		i++;
	}
	return (r);
}
