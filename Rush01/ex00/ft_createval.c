#include <stdio.h>

void	ft_createval(char	**argv, int val[4][4])
{
	int	i;
	int	j;
	int	k;

	k = 0;
	while (argv[1][k] != '\0')
	{
		i = 0;
		while (i < 4)
		{
			j = 0;
			while (j < 4)
			{
				if (k % 2 == 0)
				{
					val[i][j] = argv[1][k] - '0';
					j++;
				}
				k++;
			}
			i++;
		}
	}
}
